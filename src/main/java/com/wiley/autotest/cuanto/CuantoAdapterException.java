package com.wiley.autotest.cuanto;

public class CuantoAdapterException extends Exception {
	public CuantoAdapterException() {
		super();
	}

	public CuantoAdapterException(String message) {
		super(message);
	}

	public CuantoAdapterException(String message, Throwable cause) {
		super(message, cause);
	}

	public CuantoAdapterException(Throwable cause) {
		super(cause);
	}
}
